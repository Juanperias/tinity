pub enum Reg {
    Zero,
    Ra,
    Sp,
    Gp,
    Tp,
    T0,
    T1,
    T2,
    S0,
    S1,
    A0,
    A1,
    A2,
    A3,
    A4,
    A5,
    A6,
    A7,
    S2,
    S3,
    S4,
    S5,
    S6,
    S7,
    S8,
    S9,
    S10,
    S11,
    T3,
    T4,
    T5,
    T6,
    Mstatus,
    Mepc,
    Mtvec,
    Mcause
}

impl Into<u64> for Reg {
    fn into(self) -> u64 {
        match self {
            Reg::Zero => 0x00,
            Reg::Ra => 0x01,
            Reg::Sp => 0x02,
            Reg::Gp => 0x03,
            Reg::Tp => 0x04,
            Reg::T0 => 0x05,
            Reg::T1 => 0x06,
            Reg::T2 => 0x07,
            Reg::S0 => 0x08,
            Reg::S1 => 0x09,
            Reg::A0 => 0x0A,
            Reg::A1 => 0x0B,
            Reg::A2 => 0x0C,
            Reg::A3 => 0x0D,
            Reg::A4 => 0x0E,
            Reg::A5 => 0x0F,
            Reg::A6 => 0x10,
            Reg::A7 => 0x11,
            Reg::S2 => 0x12,
            Reg::S3 => 0x13,
            Reg::S4 => 0x14,
            Reg::S5 => 0x15,
            Reg::S6 => 0x16,
            Reg::S7 => 0x17,
            Reg::S8 => 0x18,
            Reg::S9 => 0x19,
            Reg::S10 => 0x1A,
            Reg::S11 => 0x1B,
            Reg::T3 => 0x1C,
            Reg::T4 => 0x1D,
            Reg::T5 => 0x1E,
            Reg::T6 => 0x1F,
            Reg::Mstatus => 0x300,
            Reg::Mepc => 0x341,
            Reg::Mtvec => 0x305,
            Reg::Mcause => 0x342
        }
    }
}
